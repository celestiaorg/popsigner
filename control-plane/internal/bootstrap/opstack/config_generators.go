// GenerateEnvExample generates the .env.example file content.
func GenerateEnvExample(cfg *DeploymentConfig, addrs *ContractAddresses) string {
	// POPSigner endpoint
	popsignerRPC := cfg.POPSignerEndpoint
	if popsignerRPC == "" {
		popsignerRPC = "https://rpc.popsigner.com"
	}

	// Get dispute game factory address
	disputeGameFactory := addrs.DisputeGameFactoryProxy
	if disputeGameFactory == "" {
		disputeGameFactory = "<set_after_deployment>"
	}

	// Determine L1 beacon URL based on chain ID
	l1BeaconURL := "https://ethereum-sepolia-beacon-api.publicnode.com"
	celestiaNetwork := "mocha-4"
	if cfg.L1ChainID == 1 {
		l1BeaconURL = "https://ethereum-beacon-api.publicnode.com"
		celestiaNetwork = "celestia"
	} else if cfg.L1ChainID == 17000 {
		l1BeaconURL = "https://ethereum-holesky-beacon-api.publicnode.com"
	}

	// Generate namespace if not set
	celestiaNamespace := cfg.CelestiaNamespace
	if celestiaNamespace == "" {
		celestiaNamespace = fmt.Sprintf("0000000000000000000000000000000000000000%08x", cfg.ChainID)
	}

	// Celestia key ID
	celestiaKeyID := cfg.CelestiaKeyID
	if celestiaKeyID == "" {
		celestiaKeyID = "<your-celestia-key-uuid>"
	}

	return fmt.Sprintf(`################################################################################
#                         %s - OP Stack Configuration
################################################################################
# Generated by POPKins - https://popkins.popsigner.com
# 
# Copy this file to .env and fill in the REQUIRED fields.

################################################################################
# REQUIRED - You MUST fill these in
################################################################################

# Your POPSigner API key (get from dashboard.popsigner.com)
POPSIGNER_API_KEY=<REQUIRED>

# L1 RPC endpoint (Alchemy, Infura, QuickNode, or self-hosted)
L1_RPC_URL=%s

# L1 Beacon API endpoint (required for op-node)
L1_BEACON_URL=%s

################################################################################
# CELESTIA DA CONFIGURATION - REQUIRED
################################################################################
# You need a Celestia node provider (QuickNode, etc.) or run your own node.

# Your Celestia key ID from POPSigner (UUID format)
CELESTIA_KEY_ID=%s

# Celestia Bridge/Light Node RPC (for reading blobs)
# Example: https://your-provider.celestia-mocha.quiknode.pro/your-token/
CELESTIA_BRIDGE_ADDR=<REQUIRED>
CELESTIA_BRIDGE_AUTH_TOKEN=

# Celestia Core gRPC (for submitting blobs)
# Example: your-provider.celestia-mocha.quiknode.pro:9090
CELESTIA_GRPC_ADDR=<REQUIRED>
CELESTIA_GRPC_AUTH_TOKEN=<your-token-if-required>

################################################################################
# PRE-CONFIGURED - Usually no changes needed
################################################################################

# L1 RPC type (basic, quicknode, erigon, nethermind, geth)
L1_RPC_KIND=basic

# Your L2 chain ID
CHAIN_ID=%d

# Celestia network: mocha-4 (testnet) or celestia (mainnet)
CELESTIA_NETWORK=%s

# Celestia namespace (auto-generated from chain ID)
CELESTIA_NAMESPACE=%s

# POPSigner RPC endpoint (for op-node, op-batcher signing)
POPSIGNER_RPC_URL=%s

# Role addresses (managed by POPSigner)
SEQUENCER_ADDRESS=%s
BATCHER_ADDRESS=%s
PROPOSER_ADDRESS=%s

# Contract addresses
DISPUTE_GAME_FACTORY_ADDRESS=%s
`,
		cfg.ChainName,
		cfg.L1RPC,
		l1BeaconURL,
		celestiaKeyID,
		cfg.ChainID,
		celestiaNetwork,
		celestiaNamespace,
		popsignerRPC,
		cfg.SequencerAddress,
		cfg.BatcherAddress,
		cfg.ProposerAddress,
		disputeGameFactory,
	)
}

